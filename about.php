      <h2 class="top-heading">Introduction</h2>
      <p>
        Karnataka Rajya Mahavidyalaya Shikshak Sangha (KRMSS) is an organization of college and university teachers of Karnataka and an affiliate of Akhil Bhartiya Rashtriya Shaikshik Mahasangh, Delhi (ABRSM). It was established with an objective to work in harmony with academic principles, teachers, students, and society at large. It is a novel kind of organization aims to inculcate cultural values and regain the status of the teacher as a responsible nation builder. It is an organization that focuses on creating an academic ambiance in the institutions of higher learning by developing the intellectual and spiritual capabilities in the teacher. We believe that the teachersâ€™ organization is an integral part of the educational community.
      </p>
      <p>
        The KRMSS is mainly concerned about the quality of education and the role of the teacher in building the knowledge society. We strive for excellence in teaching and find satisfaction in the development of the student community. The KRMSS also intends to stress quality research that adds to the knowledge base. Instead of blaming the system for the present condition, the KRMSS wants to play a participatory role in bringing a positive change by revisiting the problems and finding viable solutions. With Bharatiyata (Indianness) at the focal point, it aims to propagate the ideology of Cultural Nationalism in the field of education and society. It supports the value system that regains the grandeur of Indian Culture through higher education.
      </p>
      <p>
        The KRMSS believes in such education which is based on solid foundations of the culture of the land. It is also aimed at safeguarding teachers' interests including their salary, allowances, service conditions, and other facilities. Launched in the month of August 2015, the Sangha actively engaged itself with a round table meet on National Education Policy and held a series of academic discussions on issues of importance for both the state and nation in matters connected to education, teacher's welfare, and teacher development.
      </p>
